#!/usr/bin/env node
/**
 * Inline bridge/src/intents/bless-list.json into a TS module at build time.
 *
 * Why: tsup bundles bridge/src/index.ts → dist/index.js and does NOT bundle
 * non-JS assets. The runtime read in loader.ts via import.meta.url worked in
 * vitest (sources on disk) but failed at runtime against the bundle —
 * dist/intents/ doesn't exist. Bridge boot would die with ENOENT before any
 * tool call could even run scope validation.
 *
 * Same fix pattern as inline-skills.mjs: codegen the asset into a TS module
 * so it becomes part of the compiled output. Zero runtime fs reads, zero
 * path math, no shipping JSON next to the bundle.
 *
 * Source of truth: bridge/src/intents/bless-list.json
 * Output:          bridge/src/intents/bless-list.generated.ts (gitignored-safe)
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..');
const sourceFile = resolve(repoRoot, 'src', 'intents', 'bless-list.json');
const outFile = resolve(repoRoot, 'src', 'intents', 'bless-list.generated.ts');

const raw = readFileSync(sourceFile, 'utf8');
const parsed = JSON.parse(raw);

if (!parsed?.entries || !Array.isArray(parsed.entries) || parsed.entries.length === 0) {
  throw new Error(
    `inline-bless-list: ${sourceFile} parsed but has no entries[] — refusing to ship an empty bless-list`,
  );
}

const banner = `// AUTOGENERATED by bridge/scripts/inline-bless-list.mjs — do not edit by hand.
// Source: bridge/src/intents/bless-list.json
// Regenerated on every build via the "prebuild" / "pretest" scripts.
`;

const body = `${banner}
export const INLINED_BLESS_LIST: unknown = ${JSON.stringify(parsed, null, 2)};
`;

writeFileSync(outFile, body, 'utf8');

// eslint-disable-next-line no-console
console.error(
  `inline-bless-list: wrote ${outFile} (${parsed.entries.length} entries, ${raw.length}c)`,
);
